    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<Patient> list = new ArrayList<>();
        String community = jComboBox1.getSelectedItem().toString();
        for(Patient p : pd.getPatients()){
            ArrayList<Encounter> e = new ArrayList<>();
             if(community.equalsIgnoreCase(p.getPerson().getCommunity().getCommunityName()))
                 e = p.getEncounterHistory().getEncounter();
             for(Encounter encouner : e){
                 if(encouner.getVitalSigns().getNormalBP().equalsIgnoreCase("Abnormal")){
                    list.add(p);
                 }
             }
        }
        populateTable(list);
       
    }//GEN-LAST:event_jButton1ActionPerformed


