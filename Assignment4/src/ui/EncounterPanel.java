    private void populateTable(ArrayList<Patient> list) {
                 DefaultTableModel model = (DefaultTableModel) tableCar.getModel();
         model.setRowCount(0);
         
        int i = 0, total = 0;
       // for(int i = 0; i<){
            Object[] o = new Object[11];
            for(Patient p: list){
              
            o[i++] = p.getPerson().getId();
            o[i++] = p.getPerson().getName();
            o[i++] = p.getPerson().getAge();
            o[i++] = p.getPerson().getContactNo();
            o[i++] = p.getPerson().getGender();
            o[i++] = p.getPerson().getCommunity().getCommunityName();
            o[i++] = p.getPerson().getCity().getCityName();
            o[i++] = p.getPerson().getHouse().getZipcode();
            o[i++] = "Abnormal";
            
            i=0;
            total++;
            model.addRow(o);
            jLabel2.setText(String.valueOf(total));
        }
    }
